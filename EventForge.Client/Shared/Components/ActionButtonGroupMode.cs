namespace EventForge.Client.Shared.Components;

/// <summary>
/// Defines the display mode for ActionButtonGroup component
/// </summary>
public enum ActionButtonGroupMode
{
    /// <summary>
    /// Display buttons in a horizontal row (default)
    /// </summary>
    Row,
    
    /// <summary>
    /// Display buttons in a toolbar layout
    /// </summary>
    Toolbar,
    
    /// <summary>
    /// Display buttons in a vertical column
    /// </summary>
    Column
}